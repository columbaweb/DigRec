function grid_1_shortcode( $atts, $content ) {
	extract(shortcode_atts(array( 'style' => ''), $atts)); 
	$html = '<div class="grid_1' . 
				do_shortcode(extract_attr($atts)) . '" style="' . $style. '"><p>' . 
				do_shortcode($content) . '</p></div>'; 
	return $html;
}

